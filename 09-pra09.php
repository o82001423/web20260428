    <H3>直角三角形</H3>
    <?php 
    // 外層 for：這是在蓋「樓層」，從小到大蓋 5 層樓
    for($i=0; $i<5; $i++){
        // 內層 for：這是在每一層樓放「星星積木」
        // 條件 $j <= $i：第 0 樓放 1 顆，第 1 樓放 2 顆...依此類推
        for($j=0; $j<=$i; $j++){
            echo "*"; // 點一下星星
        }
        echo "<br>"; // 這層印完了，叫小兵「換到下一行」去等
    }
    ?>

    <H3>倒三角形</H3>
    <?php 
    // 外層 for：這次小兵從 5 樓開始「往下蓋」
    for($i=5; $i>0; $i--){
        // 現在是幾樓，內層就要印幾顆星星
        for($j=1; $j<=$i; $j++){
            echo "*";
        }
        echo "<br>"; // 蓋完一層，換下一行
    }
    ?>

    <H3>正三角形</H3>
    <?php 
    // 這裡總共蓋 5 層
    for($i=0; $i<5; $i++){
        // 【第一步】先放「透明積木」(空格)，把星星往中間推
        // 第一層要推 4 格，第二層推 3 格...越來越少
        for($j=0; $j<(4-$i); $j++){
            echo "&nbsp;"; // &nbsp; 是網頁專用的「隱形積木」
        }
        
        // 【第二步】放星星積木
        // 數學公式 (2*$i+1)：讓星星變成 1, 3, 5, 7, 9 顆，形成對稱的山形
        for($k=0; $k<(2*$i+1); $k++){
            echo "*";
        }
        echo "<br>"; // 山的一層蓋好了，換下一行
    }
    ?>

    <H3>菱形</H3>
    <?php 
    // 菱形就是先蓋一個「正三角形」，再蓋一個「倒過來的山」
    
    // 【上半部】蓋正三角形
    for($i=0; $i<5; $i++){
        for($j=0; $j<(4-$i); $j++){ echo "&nbsp;"; }
        for($k=0; $k<(2*$i+1); $k++){ echo "*"; }
        echo "<br>";
    }
    
    // 【下半部】蓋縮回去的部分，從 3 層開始蓋回去
    for($i=3; $i>=0; $i--){
        for($j=0; $j<(4-$i); $j++){ echo "&nbsp;"; }
        for($k=0; $k<(2*$i+1); $k++){ echo "*"; }
        echo "<br>";
    }
    ?>

    <H3>矩形</H3>
    <?php 
    // 蓋一個 7x7 的大廣場
    for($i=0; $i<7; $i++){
        for($j=0; $j<7; $j++){
            // 【判斷時間】小兵在蓋每一格前，會問自己這幾個問題：
            // 如果是在第 0 層(屋頂)、最後一層(地板)、左邊、或是右邊
            if($i==0 || $i==6 || $j==0 || $j==6){
                echo "*"; // 只要符合其中一個，就蓋星星牆壁
            } else {
                echo "&nbsp;"; // 其他中間的地方，全部留空，放隱形積木
            }
        }
        echo "<br>"; // 一排蓋完，換行
    }
    ?>

    <H3>內含對角線的矩形</H3>
    <?php 
    // 這是最酷的高級房子
    for($i=0; $i<7; $i++){
        for($j=0; $j<7; $j++){
            // 判斷條件變多了，小兵要問：
            // 是不是牆壁？(i=0,6, j=0,6) 
            // 還是我剛好走到「左上往右下」的斜線上？($i==$j)
            // 還是我剛好走到「右上往左下」的斜線上？($i==6-$j)
            if($i==0 || $i==6 || $j==0 || $j==6 || $i==$j || $i==6-$j){
                echo "*"; // 符合條件就蓋星星，形成外框和交叉線
            } else {
                echo "&nbsp;"; // 沒星星的地方都是空的
            }
        }
        echo "<br>";
    }
    ?>

